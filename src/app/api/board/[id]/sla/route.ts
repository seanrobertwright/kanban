import {
  handleCreateSlaPolicy,
  handleListSlaPolicies,
} from "@/features/sla/server/handlers";

export async function GET(
  request: Request,
  { params }: { params: Promise<{ id: string }> }
) {
  const { id } = await params;
  return handleListSlaPolicies(request, id);
}

export async function POST(
  request: Request,
  { params }: { params: Promise<{ id: string }> }
) {
  const { id } = await params;
  return handleCreateSlaPolicy(request, id);
}
