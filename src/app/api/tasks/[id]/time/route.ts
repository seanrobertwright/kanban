import {
  handleAddTimeEntry,
  handleListTaskTime,
} from "@/features/time/server/handlers";

export async function GET(
  request: Request,
  { params }: { params: Promise<{ id: string }> }
) {
  const { id } = await params;
  return handleListTaskTime(request, id);
}

export async function POST(
  request: Request,
  { params }: { params: Promise<{ id: string }> }
) {
  const { id } = await params;
  return handleAddTimeEntry(request, id);
}
