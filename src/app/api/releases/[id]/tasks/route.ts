import {
  handleListReleaseTasks,
  handleSetTaskRelease,
} from "@/features/releases/server/handlers";

export async function GET(
  request: Request,
  { params }: { params: Promise<{ id: string }> }
) {
  const { id } = await params;
  return handleListReleaseTasks(request, id);
}

export async function POST(
  request: Request,
  { params }: { params: Promise<{ id: string }> }
) {
  const { id } = await params;
  return handleSetTaskRelease(request, id);
}
